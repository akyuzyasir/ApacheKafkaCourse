
// Apache Kafka is a distributed streaming platform. It is used for building real-time data pipelines and streaming applications.

/* 
    It is horizontally scalable, fault-tolerant and incredibly fast.

It is used for real-time streams of data, to collect big data, or to do real-time analysis or to do real-time processing data.

    - Kafka Features

        * Scalablitiy: Kafka can handle large amounts of data per second. It can be scaled up to handle more data by adding more nodes to the cluster.
        * Durability: Kafka stores all published records for a configurable amount of time, even if the data is consumed.
        * Fault tolerance: Kafka is distributed and replicated, which means that it can handle node failures while maintaining data availability.
        * High throughput: Kafka can process millions of records per second, making it suitable for large-scale applications.
        * Publish-subscribe model: Kafka uses a publish-subscribe model, where producers publish data to topics and consumers subscribe to topics to receive data.
        * Low latency: Kafka has low latency, which means that it can process data quickly and deliver it to consumers in real-time.
        * Support for multiple languages: Kafka has client libraries for multiple programming languages, making it easy to integrate with different systems.
        * Integration with other systems: Kafka can be integrated with other systems, such as Hadoop, Spark, Elasticsearch, and Storm, to build real-time data pipelines and streaming applications.
        
        * Other queue systems like RabbitMQ, ActiveMQ, etc. are also available and can be used for low frequency messaging. Kafka is better suited for high frequency messaging.
        * In the case of continuous data processing, Kafka is better suited than other messaging systems.
        

